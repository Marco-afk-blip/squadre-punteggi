function editTeam(name) {
    if (!hasAccess) {
      alert('Non hai i permessi per modificare le squadre.');
      return;
    }

    const team = teams.find(t => t.name === name);
    if (!team) return;

    document.getElementById('team-name').value = team.name;
    document.getElementById('capitano-maschile').value = team.maleCaptain;
    document.getElementById('capitano-femminile').value = team.femaleCaptain;

    selectedMaleTeam = { ...team.maleTeam };
    selectedFemaleTeam = { ...team.femaleTeam };

    // Reset dropdowns
    specialties.forEach(specialty => {
      const maleDropdown = document.getElementById(`male-${specialty.replace(/ /g, '-')}`);
      const femaleDropdown = document.getElementById(`female-${specialty.replace(/ /g, '-')}`);
      
      if (maleDropdown && femaleDropdown) {
        maleDropdown.value = '';
        femaleDropdown.value = '';
        maleDropdown.disabled = false;
        femaleDropdown.disabled = false;
      }
    });

    // Set selected athletes in dropdowns
    Object.entries(team.maleTeam).forEach(([specialty, athletes]) => {
      const dropdown = document.getElementById(`male-${specialty.replace(/ /g, '-')}`);
      if (dropdown && athletes.length > 0) {
        dropdown.value = athletes[0].name;
        dropdown.disabled = true;
      }
    });

    Object.entries(team.femaleTeam).forEach(([specialty, athletes]) => {
      const dropdown = document.getElementById(`female-${specialty.replace(/ /g, '-')}`);
      if (dropdown && athletes.length > 0) {
        dropdown.value = athletes[0].name;
        dropdown.disabled = true;
      }
    });

    // Update the display
    displayTeamMembers();

    // Change the submit button text and functionality
    submitTeamButton.textContent = 'Aggiorna Squadra';
    submitTeamButton.onclick = () => updateTeam(name);
  }


  async function updateTeam(oldName) {
        if (!hasAccess) {
            alert('Non hai i permessi per aggiornare le squadre.');
            return;
        }
        const newName = document.getElementById('team-name').value;
        const maleCaptain = document.getElementById('capitano-maschile').value;
        const femaleCaptain = document.getElementById('capitano-femminile').value;
        if (validateSelection()) {
            const updatedTeam = {
                name: newName,
                maleCaptain,
                femaleCaptain,
                maleTeam: selectedMaleTeam,
                femaleTeam: selectedFemaleTeam
            };
            try {
                const response = await fetch('/.netlify/functions/updateTeam', {
                    method: 'POST',
                    body: JSON.stringify(updatedTeam)
                });
                if (!response.ok) throw new Error('Failed to update team');
                const index = teams.findIndex(t => t.name === oldName);
                if (index !== -1) {
                    teams[index] = updatedTeam;
                }
                alert('Squadra aggiornata con successo!');
                resetForm();
                displayTeams();
            } catch (error) {
                console.error('Error updating team:', error);
                alert('Si è verificato un errore durante l\'aggiornamento della squadra');
            }
        }
    }

    async function deleteTeam(name) {
        if (!hasAccess) {
            alert('Non hai i permessi per eliminare le squadre.');
            return;
        }
        if (confirm(`Sei sicuro di voler eliminare la squadra "${name}"?`)) {
            try {
                const response = await fetch('/.netlify/functions/deleteTeam', {
                    method: 'POST',
                    body: JSON.stringify({ name })
                });
                if (!response.ok) throw new Error('Failed to delete team');
                teams = teams.filter(team => team.name !== name);
                displayTeams();
            } catch (error) {
                console.error('Error deleting team:', error);
                alert('Si è verificato un errore durante l\'eliminazione della squadra');
            }
        }
    }

