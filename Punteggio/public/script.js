    function handleAthleteSelection(event) {
        if (!hasAccess) return;

        const target = event.target;
        const selectedOption = target.options[target.selectedIndex];
        const athleteName = selectedOption ? selectedOption.text : '';
        const gender = target.id.startsWith('male') ? 'male' : 'female';
        const specialty = target.id.replace(/male-|female-/, '').replace(/-/g, ' ');

        // Verifica se l'atleta è già stato aggiunto
        if (athleteName && specialties.includes(specialty)) {
            if (gender === 'male') {
                if (!selectedMaleTeam[specialty]) {
                    selectedMaleTeam[specialty] = [];
                }
                if (selectedMaleTeam[specialty].length === 0) {
                    selectedMaleTeam[specialty].push({ name: athleteName, points: 0 }); // Punti di default a 0
                }
            } else if (gender === 'female') {
                if (!selectedFemaleTeam[specialty]) {
                    selectedFemaleTeam[specialty] = [];
                }
                if (selectedFemaleTeam[specialty].length === 0) {
                    selectedFemaleTeam[specialty].push({ name: athleteName, points: 0 }); // Punti di default a 0
                }
            }

            displayTeamMembers();

            // Disabilita il menu a discesa per la specialità selezionata
            target.disabled = true;
        }
    }

